function TimelineView({ session, setSelected, setView, onTokenBump }) {
  const baseEvents = TIMELINES[session.id] || TIMELINES[SESSIONS[0].id];
  const [filter, setFilter] = useState("all");
  const [scope, setScope] = useState("this"); // "this" | "all" (incl. sub-agents, recursively)
  const [windowMs, setWindowMs] = useState(0); // 0 === "all"
  const [expanded, setExpanded] = useState(new Set());

  // descendant sessions (sub-agents, sub-sub-agents, …) that actually have timelines
  const descendants = useMemo(
    () => SESSIONS.filter((s) => isDescendantOf(s, session) && TIMELINES[s.id]),
    [session.id]
  );
  // reset scope to "this" when switching to a session with no descendants
  useEffect(() => { if (descendants.length === 0) setScope("this"); }, [session.id]);

  // --- live tail: stands in for the SSE stream so the enter/update
  //     animations are demonstrable on this static mock ---
  const [live, setLive] = useState(false);
  const [liveEvents, setLiveEvents] = useState([]);
  const [liveTotal, setLiveTotal] = useState(session.tokens_used);
  const liveTotalRef = useRef(session.tokens_used);
  const streamRef = useRef(null);

  // reset live state whenever the viewed session changes
  useEffect(() => {
    setLiveEvents([]);
    setLive(false);
    setLiveTotal(session.tokens_used);
    liveTotalRef.current = session.tokens_used;
  }, [session.id]);

  // synthetic event generator
  useEffect(() => {
    if (!live) return;
    let n = 0;
    const id = setInterval(() => {
      n += 1;
      const ts = new Date();
      const stamp = ts.getTime();
      const r = Math.random();
      let ev;
      if (n % 4 === 0) {
        const bump = 900 + Math.floor(Math.random() * 4200);
        liveTotalRef.current += bump;
        setLiveTotal(liveTotalRef.current);
        if (onTokenBump) onTokenBump((b) => b + bump);
        const total = liveTotalRef.current;
        ev = { _id: "lv-" + stamp, _new: true, ts, kind: "token_count",
          total, input: Math.round(total * 0.82), output: Math.round(total * 0.18),
          cached: Math.round(total * 0.82 * 0.55),
          ctx_pct: Math.min(99, total / 2000), rate_pct: 42 + Math.floor(Math.random() * 34) };
      } else if (r < 0.42) {
        const tools = ["read_file", "grep", "edit_file", "run_terminal", "list_dir"];
        const name = tools[Math.floor(Math.random() * tools.length)];
        ev = { _id: "lv-" + stamp, _new: true, ts, kind: "tool_call", name,
          call_id: "c" + stamp, args: { path: "src/" + name + ".ts" } };
      } else if (r < 0.66) {
        ev = { _id: "lv-" + stamp, _new: true, ts, kind: "assistant", phase: "working",
          text: "Continuing analysis of the current task context…" };
      } else if (r < 0.84) {
        ev = { _id: "lv-" + stamp, _new: true, ts, kind: "tool_call", name: "spawn_agent",
          call_id: "c" + stamp, args: { nickname: "SCOUT-" + (n % 9), role: "worker", task: "investigate subsystem" } };
      } else {
        ev = { _id: "lv-" + stamp, _new: true, ts, kind: "warning",
          text: "Approaching context window soft limit." };
      }
      setLiveEvents((prev) => [...prev, ev]);
    }, 1700);
    return () => clearInterval(id);
  }, [live, session.id]);

  // Tag events with their source session so the list can show which agent each
  // came from (depth bars + name). In "all" scope, merge the current session's
  // events with every descendant's, sorted chronologically.
  const allEvents = useMemo(() => {
    const tag = (evs, src) => {
      const d = sessionDepth(src);
      const nm = src.thread_source === "subagent" ? (src.agent_nickname || "SUB") : "ROOT";
      return evs.map((e) => ({ ...e, _srcId: src.id, _srcName: nm, _srcDepth: d }));
    };
    const own = tag([...baseEvents, ...liveEvents], session);
    if (scope === "this") return own;
    let arr = own.slice();
    for (const d of descendants) arr = arr.concat(tag(TIMELINES[d.id] || [], d));
    // always strictly by event time, regardless of agent depth
    arr.sort((a, b) => a.ts - b.ts);
    return arr;
  }, [baseEvents, liveEvents, scope, descendants, session.id]);

  // newest is on top now — keep the stream pinned to the top while tailing
  useEffect(() => {
    if (live && streamRef.current) {
      streamRef.current.scrollTop = 0;
    }
  }, [liveEvents, live]);

  // Reference time = last event in the full session. Windowing trims earlier events;
  // the scrubber rail, tab counts and stream all reflect the same windowed slice.
  const refNow = allEvents[allEvents.length - 1].ts;
  const events = useMemo(
    () => (windowMs ? allEvents.filter((e) => refNow - e.ts <= windowMs) : allEvents),
    [allEvents, windowMs, refNow]
  );

  // Guard against an empty window (shouldn't happen since refNow is in-range, but safe)
  const t0 = (events[0] || allEvents[0]).ts;
  const tEnd = (events[events.length - 1] || allEvents[allEvents.length - 1]).ts;
  const span = Math.max(1, tEnd - t0);

  // per-source token deltas (chronological, so each agent's "Δ since last" is correct)
  const _prevBySrc = {};
  events.forEach((e) => {
    if (e.kind === "token_count") { e._delta = e.total - (_prevBySrc[e._srcId] || 0); _prevBySrc[e._srcId] = e.total; }
  });

  const filtered = events.filter((e) => {
    if (filter === "all") return true;
    if (filter === "msg") return e.kind === "user" || e.kind === "assistant" || e.kind === "agent_report";
    if (filter === "tool") return e.kind === "tool_call" || e.kind === "tool_output";
    if (filter === "skill") return e.kind === "skill_invoke";
    if (filter === "agent") return e.kind === "tool_call" && (e.name === "spawn_agent" || e.name === "wait_agent");
    if (filter === "warn") return e.kind === "warning" || (e.kind === "tool_output" && e.fail);
    if (filter === "token") return e.kind === "token_count";
    return true;
  });

  // join tool_call ↔ tool_output by call_id
  const outByCall = {};
  events.forEach((e) => { if (e.kind === "tool_output") outByCall[e.call_id] = e; });

  const toggle = (key) => {
    setExpanded((prev) => {
      const n = new Set(prev);
      if (n.has(key)) n.delete(key); else n.add(key);
      return n;
    });
  };

  // build scrubber dots
  const scrubDots = events.map((e, i) => ({
    pct: ((e.ts - t0) / span) * 100,
    kind: e.kind,
    i,
  }));

  return (
    <div className="timeline">
      {/* CENTER */}
      <section className="tl-main">
        <div className="tl-tabs">
          {[
            ["all", "All Events", events.length],
            ["msg", "Messages", events.filter((e) => e.kind === "user" || e.kind === "assistant" || e.kind === "agent_report").length],
            ["tool", "Tools", events.filter((e) => e.kind === "tool_call").length],
            ["skill", "Skills", events.filter((e) => e.kind === "skill_invoke").length],
            ["agent", "Agent Ops", events.filter((e) => e.kind === "tool_call" && (e.name === "spawn_agent" || e.name === "wait_agent")).length],
            ["token", "Tokens", events.filter((e) => e.kind === "token_count").length],
            ["warn", "Warnings", events.filter((e) => e.kind === "warning" || (e.kind === "tool_output" && e.fail)).length],
          ].map(([k, l, n]) => (
            <button key={k} data-on={filter === k} onClick={() => setFilter(k)}>
              {l} <span className="muted">·{n}</span>
            </button>
          ))}
          <div className="tl-range" role="group" aria-label="Time window">
            {descendants.length > 0 && (
              <>
                <span className="tl-range-lbl">▸ SCOPE</span>
                <button
                  data-on={scope === "this"}
                  onClick={() => setScope("this")}
                  title="Only events from this agent session"
                >THIS</button>
                <button
                  data-on={scope === "all"}
                  onClick={() => setScope("all")}
                  title="Include events from sub-agents and their sub-agents"
                >+SUBS</button>
                <span className="tl-range-div" aria-hidden="true"></span>
              </>
            )}
            <button
              className="tl-live-btn"
              data-on={live}
              onClick={() => setLive((v) => !v)}
              title={live ? "Pause live tail" : "Simulate live SSE stream"}
            >
              <span className="dot"></span>{live ? "LIVE" : "TAIL"}
            </button>
            <span className="tl-range-lbl">▸ WINDOW</span>
            {[
              [60 * 60 * 1000, "1H"],
              [4 * 60 * 60 * 1000, "4H"],
              [12 * 60 * 60 * 1000, "12H"],
              [0, "ALL"],
            ].map(([ms, l]) => (
              <button
                key={l}
                data-on={windowMs === ms}
                onClick={() => setWindowMs(ms)}
                title={ms ? `Show events from last ${l}` : "Show entire session"}
              >
                {l}
              </button>
            ))}
          </div>
        </div>

        <div className="tl-scrubber">
          <div className="hdr">
            <span><Reticle /> TURN 01 · {windowMs ? `LAST ${windowMs/3600000|0}H` : "TASK_STARTED → TASK_COMPLETE"} · DUR {Math.round((tEnd - t0)/1000)}s</span>
            <span>TTFT {session.ttft_ms}ms</span>
          </div>
          <div className="track">
            {scrubDots.map((d) => (
              <span key={d.i} style={{
                position: "absolute",
                left: `${d.pct}%`,
                top: "50%",
                width: d.kind === "token_count" ? 2 : 4,
                height: d.kind === "token_count" ? 28 : (d.kind === "warning" ? 22 : 14),
                transform: "translate(-50%, -50%)",
                background:
                  d.kind === "warning" ? "var(--warn)"
                  : d.kind === "tool_call" ? "var(--amber)"
                  : d.kind === "skill_invoke" ? "var(--skill)"
                  : d.kind === "user" ? "var(--cyan)"
                  : d.kind === "agent_report" ? "var(--good)"
                  : d.kind === "task_complete" ? "var(--ink-strong)"
                  : "var(--primary)",
                boxShadow: "0 0 6px currentColor",
                color: "var(--primary)",
              }}></span>
            ))}
            {/* axis ticks */}
            {[0, 25, 50, 75, 100].map((p) => (
              <span key={p} style={{
                position: "absolute", left: `${p}%`, bottom: 0, width: 1, height: 6, background: "var(--rule-strong)",
              }}></span>
            ))}
          </div>
        </div>

        <div className="tl-stream" ref={streamRef}>
          {(() => {
            // display newest-first; in "+SUBS" scope each row carries a left gutter
            // with depth bars + agent name so you can read each event's origin
            const display = filtered.slice().reverse();
            const out = [];
            display.forEach((e, i) => {
              const key = e._id || `${e._srcId || ""}-${e.ts.getTime()}-${i}`;
              const isOpen = expanded.has(key);
              const showSrc = scope === "all";
              out.push(
                <div key={key} className={"ev-wrap" + (e._new ? " ev-enter" : "") + (showSrc ? " with-src" : "")}>
                  {showSrc && (
                    <div className="ev-src-rail" data-tone={toneForDepth(e._srcDepth)} title={`${e._srcName} · ${depthLabel(e._srcDepth)}`}>
                      <span className="ev-src-bars" aria-hidden="true">
                        {Array.from({ length: e._srcDepth + 1 }).map((_, j) => <span key={j} className="ev-src-bar" data-lvl={Math.min(j, 2)}></span>)}
                      </span>
                      <span className="ev-src-name">{e._srcName}</span>
                    </div>
                  )}
                  <EventRow e={e} delta={e._delta} outByCall={outByCall} isOpen={isOpen} toggle={() => toggle(key)} onJump={(id) => { setSelected(id); }} />
                </div>
              );
            });
            return out;
          })()}
        </div>
      </section>

      {/* RIGHT — summary panels */}
      <aside className="tl-side-r">
        <ThreadNav session={session} onSelect={setSelected} liveTotal={liveTotal} />
        <div className="tl-vitals">
        <div className="panel-tit"><span className="dot"></span><span>Turn 01 · Vitals</span><span className="spacer"></span><span className="meta">live</span></div>
        <div style={{ padding: 14, overflow: "auto" }}>
          <div className="display" style={{ fontSize: 28, color: "var(--ink-strong)" }}>
            <AnimatedNumber value={liveTotal} format={(n) => (n / 1000).toFixed(1) + "K"} />
          </div>
          <div className="kicker" style={{ marginTop: -2 }}>Σ tokens · used {live && <span className="warn-c blink">· live</span>}</div>

          <div style={{ marginTop: 16 }}>
            <div className="flex between mb-1" style={{ fontSize: 10, letterSpacing: "0.18em", color: "var(--ink-dim)", textTransform: "uppercase" }}>
              <span>Context window</span><span className="num strong"><AnimatedNumber value={Math.min(99, Math.round((liveTotal / 200000) * 100))} format={(n) => Math.round(n) + "%"} /></span>
            </div>
            <SegBar count={24} value={Math.min(100, (liveTotal / 200000) * 100)} hi={liveTotal > 100000} />
          </div>

          <div style={{ marginTop: 16 }}>
            <div className="flex between mb-1" style={{ fontSize: 10, letterSpacing: "0.18em", color: "var(--ink-dim)", textTransform: "uppercase" }}>
              <span>Primary rate-limit</span><span className="num strong">54%</span>
            </div>
            <SegBar count={24} value={54} />
          </div>

          <div style={{ marginTop: 16 }}>
            <div className="flex between mb-1" style={{ fontSize: 10, letterSpacing: "0.18em", color: "var(--ink-dim)", textTransform: "uppercase" }}>
              <span>Secondary</span><span className="num strong">18%</span>
            </div>
            <SegBar count={24} value={18} />
          </div>

          <div style={{ marginTop: 20, borderTop: "1px solid var(--rule)", paddingTop: 12 }}>
            <div className="kicker mb-2">▸ Tool Usage · this turn</div>
            {Object.entries(events.filter((e) => e.kind === "tool_call").reduce((acc, e) => { acc[e.name] = (acc[e.name]||0)+1; return acc; }, {})).map(([n, c]) => (
              <div key={n} className="flex between" style={{ fontSize: 11, padding: "3px 0" }}>
                <span>{n}</span>
                <span className="num strong">{c}</span>
              </div>
            ))}
          </div>

          <button
            onClick={() => setView("graph")}
            style={{ marginTop: 16, width: "100%", padding: "10px 12px", border: "1px solid var(--primary)", color: "var(--primary)", fontFamily: "var(--display)", letterSpacing: "0.18em", textTransform: "uppercase", fontSize: 11, fontWeight: 700 }}
          >
            ▸ Open Agent Graph
          </button>
        </div>
        </div>
      </aside>
    </div>
  );
}

function EventRow({ e, delta, outByCall, isOpen, toggle, onJump }) {
  if (e.kind === "task_started") {
    return (
      <div className="ev" style={{ borderLeft: "1px solid var(--rule-strong)" }}>
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body" style={{ borderColor: "var(--primary)" }}>
          <div className="head">
            <span className="who" style={{ color: "var(--primary)" }}>TURN 01 · TASK_STARTED</span>
            <span className="right">model {e.model} · effort {e.effort} · sandbox {e.sandbox}</span>
          </div>
        </div>
      </div>
    );
  }
  if (e.kind === "task_complete") {
    return (
      <div className="ev">
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body" style={{ borderColor: "var(--good)" }}>
          <div className="head">
            <span className="who" style={{ color: "var(--good)" }}>▸ TASK_COMPLETE</span>
            <span className="right">dur {Math.round(e.duration_ms/1000)}s · ttft {e.ttft_ms}ms</span>
          </div>
          <pre>{e.last_agent_message}</pre>
        </div>
      </div>
    );
  }
  if (e.kind === "user") {
    return (
      <div className="ev user">
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body">
          <div className="head"><span className="who user">USER</span><span>message</span></div>
          <pre>{e.text}</pre>
        </div>
      </div>
    );
  }
  if (e.kind === "assistant") {
    return (
      <div className="ev assistant">
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body">
          <div className="head"><span className="who">ASSISTANT</span><span>{e.phase}</span></div>
          <pre>{e.text}</pre>
        </div>
      </div>
    );
  }
  if (e.kind === "agent_report") {
    return (
      <div className="ev spawn">
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body" style={{ borderColor: "var(--good)" }}>
          <div className="head"><span className="who spawn">{e.from} · AGENT REPORT</span></div>
          <pre>{e.text}</pre>
        </div>
      </div>
    );
  }
  if (e.kind === "skill_invoke") {
    return (
      <div className="ev skill">
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body" style={{ borderColor: "var(--skill)" }}>
          <div className="head">
            <span className="who skill">✦ SKILL · {e.name}</span>
            {e.call_id && <span>call_id {e.call_id}</span>}
            {e.status && <span className={e.status === "ok" ? "chip good" : "chip warn"} style={{ marginLeft: "auto" }}>{e.status}</span>}
          </div>
          {e.summary && <div className="args" style={{ fontSize: 11, color: "var(--ink-dim)", marginTop: 2 }}>{e.summary}</div>}
        </div>
      </div>
    );
  }
  if (e.kind === "warning") {
    return (
      <div className="ev warn">
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body" style={{ borderColor: "var(--warn)" }}>
          <div className="head"><span className="who warn">▲ WARNING</span></div>
          <pre style={{ color: "var(--warn)" }}>{e.text}</pre>
        </div>
      </div>
    );
  }
  if (e.kind === "token_count") {
    const fresh = Math.max(0, e.input - e.cached);
    const pct = (v) => `${(v / (e.total || 1)) * 100}%`;
    const cachePct = e.input ? Math.round((e.cached / e.input) * 100) : 0;
    return (
      <div className="ev token">
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body" style={{ borderColor: "var(--rule-soft)" }}>
          <div className="head">
            <span className="who">TOKEN_COUNT</span>
            <span>snapshot</span>
            {delta != null && (
              <span className="chip cyan" style={{ marginLeft: "auto" }}>
                Δ +{(delta / 1000).toFixed(1)}K since last
              </span>
            )}
          </div>
          <div className="tkc">
            <div className="tkc-total">
              <span className="num strong v">{(e.total / 1000).toFixed(1)}K</span>
              <span className="muted">total · {cachePct}% of input cached</span>
            </div>
            <div className="tkc-stack" title={`cached ${e.cached.toLocaleString()} · fresh input ${fresh.toLocaleString()} · output ${e.output.toLocaleString()}`}>
              {e.cached > 0 && <span className="seg cached" style={{ width: pct(e.cached) }}></span>}
              {fresh > 0 && <span className="seg input" style={{ width: pct(fresh) }}></span>}
              {e.output > 0 && <span className="seg output" style={{ width: pct(e.output) }}></span>}
            </div>
            <div className="tkc-legend">
              <span className="cy">cached {(e.cached / 1000).toFixed(1)}K</span>
              <span className="pr">input {(fresh / 1000).toFixed(1)}K</span>
              <span className="am">output {(e.output / 1000).toFixed(1)}K</span>
            </div>
            <div className="tkc-meters">
              <div className="tkc-meter">
                <div className="flex between"><span className="k">Context window</span><span className={"num " + (e.ctx_pct > 60 ? "warn-c" : "strong")}>{e.ctx_pct.toFixed(1)}%</span></div>
                <SegBar count={18} value={e.ctx_pct} hi={e.ctx_pct > 60} />
              </div>
              <div className="tkc-meter">
                <div className="flex between"><span className="k">Rate limit</span><span className={"num " + (e.rate_pct > 60 ? "warn-c" : "strong")}>{e.rate_pct}%</span></div>
                <SegBar count={18} value={e.rate_pct} hi={e.rate_pct > 60} />
              </div>
            </div>
          </div>
        </div>
      </div>
    );
  }
  if (e.kind === "tool_call") {
    const out = outByCall[e.call_id];
    const isSpawn = e.name === "spawn_agent";
    const isWait = e.name === "wait_agent";
    const argSummary = e.args.cmd ? e.args.cmd.join(" ")
      : (e.args.path ? e.args.path
      : (e.args.nickname ? `${e.args.nickname} (${e.args.role}) // ${e.args.task}`
      : (e.args.thread_id ? `await ${e.args.thread_id.slice(0,12)}…` : JSON.stringify(e.args))));
    return (
      <div className={`ev tool${isSpawn ? " spawn" : ""}`}>
        <div className="ts num">{fmtTimeMs(e.ts).slice(0, 12)}</div>
        <div className="body" style={{ borderColor: isSpawn ? "var(--good)" : "var(--amber)" }}>
          <div className="head">
            <span className={`who ${isSpawn ? "spawn" : "tool"}`}>
              {isSpawn ? "⊕ SPAWN_AGENT" : isWait ? "◌ WAIT_AGENT" : `▸ ${e.name.toUpperCase()}`}
            </span>
            <span>call_id {e.call_id}</span>
            {out && <span className={"chip " + (out.fail ? "warn" : "good")}>exit {out.exit} · {Math.round((out.ts - e.ts))}ms</span>}
            {isSpawn && <button onClick={() => onJump(e.spawn_thread_id)} className="chip cyan" style={{ marginLeft: "auto", cursor: "pointer" }}>↗ open child</button>}
          </div>
          <div className="args">$ {argSummary}</div>
          {out && (
            <div className={"out" + (out.fail ? " fail" : "")}>
              <div className="muted" style={{ fontSize: 10, marginBottom: 4, letterSpacing: "0.18em" }}>
                STDOUT · {(out.output || "").length} bytes {out.fail && "· FAILED"}
              </div>
              <pre style={{ fontSize: 11 }}>{out.output}</pre>
            </div>
          )}
        </div>
      </div>
    );
  }
  if (e.kind === "tool_output") return null; // handled inline with tool_call
  return null;
}

