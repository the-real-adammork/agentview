/* AnimatedNumber — tweens the displayed value whenever `value` changes and
   flashes a tint (green up / red down). Used anywhere a live count updates. */
function AnimatedNumber({ value, format = (n) => Math.round(n).toLocaleString(), className = "", duration = 700 }) {
  const [display, setDisplay] = React.useState(value);
  const [flash, setFlash] = React.useState(null); // null | 'up' | 'down'
  const fromRef = useRef(value);
  const prevRef = useRef(value);
  const rafRef = useRef(0);

  useEffect(() => {
    if (value === prevRef.current) return;
    const from = fromRef.current;
    const to = value;
    const dir = to >= prevRef.current ? "up" : "down";
    prevRef.current = value;
    const start = performance.now();
    setFlash(dir);
    cancelAnimationFrame(rafRef.current);
    const tick = (t) => {
      const p = Math.min(1, (t - start) / duration);
      const eased = 1 - Math.pow(1 - p, 3); // easeOutCubic
      const cur = from + (to - from) * eased;
      fromRef.current = cur;
      setDisplay(cur);
      if (p < 1) rafRef.current = requestAnimationFrame(tick);
      else { fromRef.current = to; setDisplay(to); setTimeout(() => setFlash(null), 350); }
    };
    rafRef.current = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(rafRef.current);
  }, [value, duration]);

  return <span className={`anim-num ${flash ? "flash-" + flash : ""} ${className}`}>{format(display)}</span>;
}

